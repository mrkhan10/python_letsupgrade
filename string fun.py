#STRING FUNCTIONS USED IN PYTHON

a = "letsupgrade"           #First String
b= "PYTHON ESSENTIALS"      #Second String

print(a.islower())           #this function checks whether the string is in lower case

print(b.isupper())           #this function checks whether the string is in upper case

print(a.capitalize())        #this function capitalizes the first letter of the word

print(a.title())             #this function makes the string in title format

print(a.upper())             #this function changes the string to upper case 

print(b.lower())             #this function changes the string to lower case

print(a.isalpha())           #this function checks whether the string is alphabetical or not

print(b.isdigit())           #this function checks whether the string is numerical or not


